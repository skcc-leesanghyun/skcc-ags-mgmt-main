package com.skcc.ags.talent.domain;

/**
 * Enum representing the possible decisions for a candidate evaluation.
 */
public enum EvaluationDecision {
    STRONGLY_RECOMMEND,    // Highly recommended for the position
    RECOMMEND,            // Recommended for the position
    NEUTRAL,              // Neither recommended nor not recommended
    NOT_RECOMMEND,        // Not recommended for the position
    STRONGLY_NOT_RECOMMEND // Strongly not recommended for the position
} 